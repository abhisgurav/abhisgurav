/* ============================== toggle style switcher ============================== */
const styleSwitcherToggle = document.querySelector(".style-switcher-toggler");
styleSwitcherToggle.addEventListener("click", () => {
    document.querySelector(".style-switcher").classList.toggle("open");
}) 
// hide style - switcher on scroll
window.addEventListener("sroll", () => {
    if(document.querySelector(".style-switcher").classList.contains("open"))
    {
        document.querySelector(".style-switcher").classList.remove("open");
    }
})
/* ============================== toggle colors ============================== */
const aleternateStyles = document.querySelectorAll(".alternate-style");
function setActiveStyle(color)
{
    aleternateStyles.forEach((style) => {
        if(color === style.getAttribute("title"))
        {
            style.removeAttribute("disabled");
        }
        else
        {
            style.setAttribute("disabled", "true");
        }
    })
}
/* ============================== theme light and dark ============================== */
const dayNight = document.querySelector(".day-night");
dayNight.addEventListener("click", () => {
    dayNight.querySelector("i").classList.toggle("fa-sun");
    dayNight.querySelector("i").classList.toggle("fa-moon");
    document.body.classList.toggle("dark");
})
window.addEventListener("load", () => {
    if(document.body.classList.contains("dark"))
    {
        dayNight.querySelector("i").classList.add("fa-sun");
    }
    else
    {
        dayNight.querySelector("i").classList.add("fa-moon");
    }
})

function checkWidthAndAddClass() {
    const sidebar = document.querySelector('.sidebar');
    if (sidebar) {
        if (window.innerWidth <= 1379) {
            sidebar.classList.add('close');
        } else {
            sidebar.classList.remove('close');
        }
    }
}

// Initial check
checkWidthAndAddClass();

// Add event listener for window resize
window.addEventListener('resize', checkWidthAndAddClass);


